/// Returns a compact dictionary-style plural ending for [noun] (e.g. '-e',
/// '-n', '¨-', '¨-er'), or null if [noun] has no entry in [nounPlurals], or
/// its plural isn't formed by a simple suffix/umlaut change (e.g. Datum ->
/// Daten) and should be shown in full instead.
String? pluralEndingNotation(String noun) {
  final plural = nounPlurals[noun];
  if (plural == null) return null;

  String deUmlaut(String s) => s
      .replaceAll('ä', 'a')
      .replaceAll('ö', 'o')
      .replaceAll('ü', 'u')
      .replaceAll('Ä', 'A')
      .replaceAll('Ö', 'O')
      .replaceAll('Ü', 'U');

  final normNoun = deUmlaut(noun);
  final normPlural = deUmlaut(plural);

  var prefixLen = 0;
  while (prefixLen < noun.length &&
      prefixLen < normPlural.length &&
      normNoun[prefixLen] == normPlural[prefixLen]) {
    prefixLen++;
  }
  if (prefixLen < noun.length) return null;

  final suffix = plural.substring(prefixLen);
  final hasUmlaut = plural.substring(0, prefixLen) != noun.substring(0, prefixLen);
  final ending = suffix.isEmpty ? '-' : '-$suffix';
  return hasUmlaut ? '¨$ending' : ending;
}

