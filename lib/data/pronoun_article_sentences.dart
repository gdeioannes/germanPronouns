/// Q&A dialogue pairs for Pronouns & Articles quiz.
/// Each pair has a question (complete) and an answer with blanks for pronouns.
///
/// Design rule for answerability: every answer is SUBJECT-FIRST. The learner
/// can always determine the order from the rule "the subject pronoun comes
/// before the object pronoun", and determine each pronoun from the question
/// (the subject noun's gender/number) and the answer verb (which signals
/// accusative vs. dative). Object-fronting is avoided because both orders
/// would be valid German, making the answer ambiguous.
class PronounArticlePair {
  const PronounArticlePair({
    required this.case_,
    required this.nominative,
    required this.question,
    required this.answerSentence,
    required this.blanks, // List of [blank1, blank2]
    required this.answer, // "pronoun1 pronoun2" or similar
    required this.pattern, // 'together' or 'separate'
  });

  final String case_; // 'accusative' or 'dative'
  final String nominative; // base pronoun of the tested object (mich->ich, etc.)
  final String question;
  final String answerSentence; // with _____ blanks
  final List<String> blanks; // positions of blanks
  final String answer; // correct answer to fill blanks
  final String pattern; // 'together' or 'separate'
}

